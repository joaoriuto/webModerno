//Repetindo com o While
let contador = 1
while(contador <= 10){
    console.log(`Contador = ${contador}`)
    contador ++
}

//Mostrando o FOR
for(let i = 1; i <= 10; i++){
    console.log(`Contador2 = ${i}`)
}

//For em arrays

const notas = [6.7, 7.5, 4.5, 2.6, 8.8]
for(let i = 0; i < notas.length; i++){
    console.log(`Notas do array: ${notas[i]}`)
}